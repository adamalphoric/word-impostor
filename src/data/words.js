export const ALL_CATEGORIES = Object.keys(WORD_PACKS)

export function getWordFromCategories(categories = ALL_CATEGORIES) {
  const validCats = categories.filter((c) => WORD_PACKS[c])
  if (!validCats.length) return null

  const catId = validCats[Math.floor(Math.random() * validCats.length)]
  const pack = WORD_PACKS[catId]
  const wordEntry = pack.words[Math.floor(Math.random() * pack.words.length)]

  return {
    ...wordEntry,
    category: pack.label,
    categoryId: catId,
  }
}
